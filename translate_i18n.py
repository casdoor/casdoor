def load_json(filepath):
    """Load JSON file"""
    with open(filepath, 'r', encoding='utf-8') as f:
        return json.load(f)

def save_json(filepath, data):
    """Save JSON file with proper formatting"""
    with open(filepath, 'w', encoding='utf-8') as f:
        json.dump(data, f, indent=2, ensure_ascii=False)
        f.write('\n')

def translate_entry(english_value, target_lang, translations_dict):
    """Translate a single entry"""
    # Check if we have a direct translation
    if english_value in translations_dict.get(target_lang, {}):
        return translations_dict[target_lang][english_value]
    
    # For untranslated entries, return the English value for now
    # We'll handle this programmatically with proper translations
    return english_value

def process_language(lang, frontend_path, backend_path, english_frontend, english_backend):
    """Process translations for a single language"""
    print(f"\nProcessing {lang}...")
    
    # Load target language files
    frontend_data = load_json(frontend_path)
    backend_data = load_json(backend_path)
    
    # Count untranslated entries
    untranslated_frontend = 0
    untranslated_backend = 0
    
    for key, value in frontend_data.items():
        if value == english_frontend.get(key):
            untranslated_frontend += 1
    
    for key, value in backend_data.items():
        if value == english_backend.get(key):
            untranslated_backend += 1
    
    print(f"  Frontend: {untranslated_frontend} untranslated entries")
    print(f"  Backend: {untranslated_backend} untranslated entries")
    
    return untranslated_frontend + untranslated_backend

